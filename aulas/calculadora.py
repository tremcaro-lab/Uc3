class Calculadora():
   def __init__(self) :
      self.total =0
   def somar(self,valor1,valor2):
      self.total =valor1 +valor2
   def subtrair(self,valor1,valor2):
      self.total= valor1- valor2
   def multiplicar(self,valor1,valor2):
      self.total = valor1*valor2
   def dividir(self,valor1,valor2):
      self.total=valor1/valor2